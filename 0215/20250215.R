# 벡터랑 for문 사용방법
numbers = c(3,8,12,5,7,10,15) # 벡터 생성
even_numbers = c() # 빈 벡터 생성
for(i in numbers){ # numbers에 있는 수 만큼 반복 - 7회 반복
  if(i %% 2 == 0){ # %% 나머지값
    even_numbers = c(even_numbers, i)
  }  
}
print(even_numbers)

