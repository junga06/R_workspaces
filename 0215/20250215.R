print(result)

score = c(70,90,100,40,60) #벡터 생성
### 80점 이상은 'A', 80 미만은 'B'

result = ifelse(score >= 80, "A" , "B")
# 벡터안에 있는 값(요소)을 전부 if/else로 필터링 한다.
print(result)

###반복문
#특정 작업을 반복적으로 수행할 때 사용
